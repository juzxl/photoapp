package com.example.photoapp.model;

/**
 * Перечисление для уровней приватности фотографий и альбомов
 */
public enum PrivacyLevel {
    /**
     * Публичный доступ - видно всем пользователям
     */
    PUBLIC,
    
    /**
     * Для друзей пользователя (в текущей реализации работает как PUBLIC)
     */
    FRIENDS,
    
    /**
     * Приватный доступ - видно только владельцу
     */
    PRIVATE
} 